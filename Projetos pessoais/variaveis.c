#include <stdio.h>
#include <stdlib.h>

int main()
{

    // %i ou %d para inteiros
    // %f para floats
    // %c para char
    // %s para strings
    // %u para modulos
    // %p para endereços de memoria
    // %e para notação cientifica
    // %% para o simbolo de porcentagem
    int x = 10;
    printf("%d\n", x);
    float y = 8.5;
    printf("%f\n", y);

    return 0;
}